package com.isa.platform.upc.Ejercicio5.shared.dto.enums;

/**
 * The EStatus enum is used for representing the status of a response.
 * It has two possible values: SUCCESS and ERROR.
 *
 * @version 1.0
 * @author Jose Arenas Conde
 * @since 30/11/2023
 */
public enum EStatus {
    /**
     * Represents a successful response.
     */
    SUCCESS,

    /**
     * Represents an erroneous response.
     */
    ERROR
}
